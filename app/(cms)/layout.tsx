import '@/app/globals.css'
import { AppSidebar } from '@/components/app-sidebar'
import { SiteHeader } from '@/components/site-header'
import { ThemeModeProvider } from '@/components/theme-mode-provider'
import { ThemeProvider } from '@/components/theme-provider'
import { SidebarInset, SidebarProvider } from '@/components/ui/sidebar'
import { requireAuth } from '@/lib/auth/session'
import { SettingsService } from '@/lib/services/settings.service'
import { TRPCReactProvider } from '@/lib/trpc/client'
import { ReactQueryDevtools } from '@tanstack/react-query-devtools'
import { Metadata } from 'next'
import { Geist, Geist_Mono } from 'next/font/google'

const geistSans = Geist({
  variable: '--font-geist-sans',
  subsets: ['latin']
})

const geistMono = Geist_Mono({
  variable: '--font-geist-mono',
  subsets: ['latin']
})

export const metadata: Metadata = {
  title: 'Blog Admin',
  description: 'Generated by create next app'
}

export default async function AdminLayout({
  children
}: {
  children: React.ReactNode
}) {
  // Require authentication (only admin emails can login)
  await requireAuth()

  // Get current theme
  const currentTheme = await SettingsService.getCurrentTheme()

  return (
    <html lang="en" suppressHydrationWarning>
      <body
        className={`${geistSans.variable} ${geistMono.variable} antialiased bg-sidebar`}
      >
        <TRPCReactProvider>
          <ReactQueryDevtools />
          <ThemeModeProvider>
            <ThemeProvider themeId={currentTheme} />
            <SidebarProvider
              style={
                {
                  '--sidebar-width': 'calc(var(--spacing) * 72)',
                  '--header-height': 'calc(var(--spacing) * 12)'
                } as React.CSSProperties
              }
            >
              <AppSidebar variant="inset" />
              <SidebarInset>
                <SiteHeader />
                <div className="flex flex-1 flex-col">
                  <div className="@container/main flex flex-1 flex-col gap-2 p-4">
                    {children}
                  </div>
                </div>
              </SidebarInset>
            </SidebarProvider>
          </ThemeModeProvider>
        </TRPCReactProvider>
      </body>
    </html>
  )
}
